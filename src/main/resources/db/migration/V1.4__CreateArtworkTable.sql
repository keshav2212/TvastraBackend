CREATE TABLE ARTWORK(
    ID BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    TITLE VARCHAR,
    DESCRIPTION VARCHAR,
    ART_IMAGE_PATH VARCHAR,
    CATEGORY_ID BIGINT,
    USER_ID BIGINT,
    GALLERY_ID BIGINT,
    AMOUNT DECIMAL,
    CURRENCY VARCHAR,
    CONSTRAINT fk_artwork_users FOREIGN KEY (USER_ID) REFERENCES USERS (ID),
    CONSTRAINT fk_artwork_category FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORY (ID),
    CONSTRAINT fk_artwork_gallery FOREIGN KEY (GALLERY_ID) REFERENCES GALLERY (ID)

);